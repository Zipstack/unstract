from django.db import models
from django.db.models import F
from django.utils import timezone


class PgQueueMessage(models.Model):
    """Bespoke extension-free queue table (``SKIP LOCKED`` + visibility timeout).

    The table and its dequeue index are fully Django-managed (generated by
    ``makemigrations``, migration 0001) — no hand-written migration, no DB
    function, no ``CREATE EXTENSION`` (UN-3533 extension-free decision). The
    ``SKIP LOCKED`` dequeue itself is a single statement in the workers'
    ``pg_queue`` client, not a DB-side function. This model doubles as a
    typed read handle for the backend producer side.
    """

    msg_id = models.BigAutoField(primary_key=True)
    queue_name = models.TextField()
    # Opaque JSONB payload. For task dispatches the workers write a task
    # payload here (``queue_backend.pg_queue.TaskPayload``: task_name / args
    # / kwargs / queue / fairness); the queue itself stays payload-agnostic.
    message = models.JSONField()
    # "" = no org (leaf tasks); the fair-admission query (a later phase)
    # uses it for the coupled pipeline. Empty string rather than NULL —
    # a string field shouldn't have two "no data" values (Django S6553).
    org_id = models.TextField(blank=True, default="")
    # Python-level defaults let ORM ``.create()`` work without these; the
    # raw-SQL client supplies them explicitly (``now()`` / ``0``), so no
    # DB-level default is required.
    enqueued_at = models.DateTimeField(default=timezone.now)
    vt = models.DateTimeField(default=timezone.now)  # visibility timeout
    read_ct = models.IntegerField(default=0)
    # fairness L3: higher value is claimed sooner. The canonical range lives in
    # workers/queue_backend/fairness.py (MIN_PRIORITY=1, MAX_PRIORITY=10,
    # DEFAULT_PRIORITY=5); these literals mirror it because backend and workers
    # are separate codebases that can't import each other. Keep them in sync — a
    # workers integration test (test_db_check_constraint_matches_fairness_bounds)
    # asserts this DB constraint matches the fairness bounds, so a divergence
    # fails loudly. The dispatch writes priority from fairness.pipeline_priority;
    # leaf tasks with no fairness get the neutral default. L1 (org tier) / L2
    # (workload) ordering + burst_max admission are deferred to the fair-admission
    # orchestrator. The CheckConstraint is the one backstop no writer can bypass.
    priority = models.SmallIntegerField(default=5)

    class Meta:
        db_table = "pg_queue_message"
        constraints = [
            models.CheckConstraint(
                check=models.Q(priority__gte=1) & models.Q(priority__lte=10),
                name="pg_queue_message_priority_range",
            ),
        ]
        indexes = [
            # The dequeue walks one queue in (priority DESC, msg_id ASC) order
            # and applies vt <= now() as a per-row filter during the walk —
            # claim high-priority first, FIFO (msg_id, monotonic and stable
            # across re-claims) within a band. Note this is NOT a guaranteed
            # top-N: vt is intentionally not in the index, so claimed-but-unacked
            # rows (future vt) sit at the front of their band and are scanned
            # past on each claim. Fine at low in-flight depth; the orchestrator's
            # staging→task admission is the answer if that backlog grows large.
            models.Index(
                F("queue_name"),
                F("priority").desc(),
                F("msg_id"),
                name="pg_queue_message_dequeue_idx",
            )
        ]


class PgBarrierState(models.Model):
    """Per-execution fan-in barrier state for ``PgBarrier`` (the Postgres
    ``WORKER_BARRIER_BACKEND``).

    One row per in-flight barrier (keyed by ``execution_id``). The worker-side
    ``barrier_pg_decr_and_check`` link task atomically decrements ``remaining``
    and appends to ``results`` in a single ``UPDATE … RETURNING``; the task that
    drives ``remaining`` to 0 dispatches the aggregating callback and deletes the
    row. A header-task failure aborts the barrier by deleting the row outright
    (``DELETE … RETURNING`` — atomic claim+teardown), so the callback can never
    fire with partial results. ``expires_at`` bounds an orphaned barrier (header
    tasks that never complete); a periodic sweep job (not yet implemented) is the
    intended reclaim backstop.

    Managed=True / generated migration — no DB-side function, extension-free
    (UN-3533), same posture as ``PgQueueMessage``.
    """

    execution_id = models.TextField(primary_key=True)
    # Header tasks still pending. The last task to decrement it to 0 fires the
    # callback. A value < 0 (decrement after expiry/cleanup) means the barrier
    # was already torn down — the task cleans up without firing.
    remaining = models.IntegerField()
    # Aggregated header-task results, appended in completion order (JSONB array).
    results = models.JSONField(default=list)
    created_at = models.DateTimeField(default=timezone.now)
    # Orphan bound (Redis-TTL equivalent): a barrier whose header tasks never
    # complete is reclaimable past this. Must exceed the longest execution
    # wall-clock, same budgeting as WORKER_BARRIER_KEY_TTL_SECONDS.
    expires_at = models.DateTimeField()

    class Meta:
        db_table = "pg_barrier_state"
        constraints = [
            # The one writer-proof invariant (the worker SQL can't import this
            # model). Deliberately NO `remaining >= 0` check — the teardown path
            # relies on `remaining` going negative as a "barrier already gone"
            # signal, so a non-negative constraint would break it.
            models.CheckConstraint(
                check=models.Q(expires_at__gt=models.F("created_at")),
                name="pg_barrier_expires_after_created",
            ),
        ]
        indexes = [
            # Drives the (future) periodic expiry-sweep job.
            models.Index(fields=["expires_at"], name="pg_barrier_expires_idx"),
        ]
