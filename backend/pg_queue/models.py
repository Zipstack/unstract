from django.db import models
from django.utils import timezone


class PgQueueMessage(models.Model):
    """Bespoke extension-free queue table (``SKIP LOCKED`` + visibility timeout).

    The table and its dequeue index are fully Django-managed (generated by
    ``makemigrations``, migration 0001) — no hand-written migration, no DB
    function, no ``CREATE EXTENSION`` (UN-3533 extension-free decision). The
    ``SKIP LOCKED`` dequeue itself is a single statement in the workers'
    ``pg_queue`` client, not a DB-side function. This model doubles as a
    typed read handle for the backend producer side.
    """

    msg_id = models.BigAutoField(primary_key=True)
    queue_name = models.TextField()
    message = models.JSONField()
    # "" = no org (leaf tasks); the fair-admission query (a later phase)
    # uses it for the coupled pipeline. Empty string rather than NULL —
    # a string field shouldn't have two "no data" values (Django S6553).
    org_id = models.TextField(blank=True, default="")
    # Python-level defaults let ORM ``.create()`` work without these; the
    # raw-SQL client supplies them explicitly (``now()`` / ``0``), so no
    # DB-level default is required.
    enqueued_at = models.DateTimeField(default=timezone.now)
    vt = models.DateTimeField(default=timezone.now)  # visibility timeout
    read_ct = models.IntegerField(default=0)

    class Meta:
        db_table = "pg_queue_message"
        indexes = [
            models.Index(
                fields=["queue_name", "vt", "msg_id"],
                name="pg_queue_message_dequeue_idx",
            )
        ]
